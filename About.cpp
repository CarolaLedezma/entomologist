#include "About.h"
#include "ui_About.h"

About::About(QWidget *parent) :
    QDialog(parent),
    ui(new Ui::About)
{
    ui->setupUi(this);
    ui->versionLabel->setText(QString("<b>Entomologist version %1</b>").arg(APP_VERSION));

    connect(ui->buttonBox, SIGNAL(accepted()),
            this, SLOT(close()));
}

About::~About()
{
    delete ui;
}


void About::changeEvent(QEvent *e)
{
    QDialog::changeEvent(e);
    switch (e->type()) {
    case QEvent::LanguageChange:
        ui->retranslateUi(this);
        break;
    default:
        break;
    }
}
